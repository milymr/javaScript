let opcao = 2
switch (opcao){
    case 1 :
        console.log("Você escolheu a opcao 1")
        break 
        case 2 :
            console.log("Você escolheu a opcao 2 ")
        break
        default: 
        console.log("Opção invalida                                                                                                                                                                                                                                                           ")
}
